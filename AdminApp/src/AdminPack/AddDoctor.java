    public void addDoctor() {
        String gender;
        String pass = new String(jPasswordField1.getPassword());
        if (jRadioButton1.isSelected()) {
            gender = "M";
        } else {
            gender = "F";
        }
        try {
            ssql = "insert into doctor values('" + jTextField5.getText() + "','" + jTextField1.getText() + "','" + jTextField2.getText() + "','" + jTextField3.getText() + "','" + pass + "','" + gender + "','" + jTextField4.getText() + "','" + jTextArea1.getText() + "','" + jTextField7.getText() + "','" + jComboBox1.getSelectedItem().toString() + "'," + jComboBox2.getSelectedItem().toString() + ")";
            System.out.println("Query :" + ssql);
            stmt = parent.con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            stmt.executeUpdate(ssql);
            JOptionPane.showMessageDialog(null, "Doctor Added Successfully");
        } catch (Exception e) {
            System.out.println("Error Fetching data : " + e);
        }

    }

